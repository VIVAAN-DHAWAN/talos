// Intentional Dark Matter: these helpers are exported but never imported anywhere.
// The scheduled cleaner should flag them for removal.

export function add(a: number, b: number): number {
  return a + b;
}

export function subtract(a: number, b: number): number {
  return a - b;
}

export function multiply(a: number, b: number): number {
  return a * b;
}
